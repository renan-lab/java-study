package com.renan.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {

		int vlr1 = 1;
		int vlr2 = 2;
		
		System.out.println("vlr1 == vlr2: " + (vlr1 == vlr2));
		System.out.println("vlr1 != vlr2: " + (vlr1 != vlr2));
		System.out.println("vlr1 > vlr2: " + (vlr1 > vlr2));
		System.out.println("vlr1 >= vlr2: " + (vlr1 >= vlr2));
		System.out.println("vlr1 < vlr2: " + (vlr1 < vlr2));
		System.out.println("vlr1 <= vlr2: " + (vlr1 <= vlr2));
		
	}

}
