package com.renan.cursojava.aula34.labs;

public class ConversorArea {
	
	public static double metroParaPes(double vlr) {
		return vlr * 10.76;
	}
	
	public static double peParaCentimetros(double vlr) {
		return vlr * 929;
	}
	
	public static double milhaParaAcres(double vlr) {
		return vlr * 640;
	}
	
	public static double acreParaPes(double vlr) {
		return vlr * 43560;
	}
	
}
